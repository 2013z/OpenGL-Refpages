<h1> OpenGL&reg; and OpenGL&reg; ES Reference Pages </h1>

<p> Complete descriptions of API commands and shading language functions are
    provided for the following versions of these APIs: </p>

<p> <b>Current API Versions:</b> </p>

<ul>
<li> <p> <a href="es3/"> OpenGL ES 3.2 and OpenGL ES Shading Language 3.20 </a>
<li> <p> <a href="gl4/"> OpenGL 4.5 and OpenGL Shading Language 4.50 </a>
</ul>

<p> <b>Older API Versions:</b> </p>

<ul>
<li> <p> <a href="es3.1/"> OpenGL ES 3.1 and OpenGL ES Shading Language 3.10 </a>
<li> <p> <a href="es3.0/"> OpenGL ES 3.0 and OpenGL ES Shading Language 3.00 </a>
<li> <p> <a href="es2.1/"> OpenGL ES 2.1 </a>
<li> <p> <a href="es1.1/"> OpenGL ES 1.1 </a>
<li> <p> <a href="gl2.1/"> OpenGL 2.1 </a>
     (inncluding GLX and fixed-function GL compatibility APIs)
</ul>


<h1> API Reference Cards </h1>

<p> <a target="_new"
    href="https://www.khronos.org/developers/reference-cards"> Quick
    reference cards</a> are available in several formats for OpenGL 3.3 -
    4.5 and OpenGL ES 2.0 - 3.2. </p>

<p> Formal Specifications for the OpenGL and OpenGL ES APIs and Shading
    Languages are available from the <a target="_new"
    href="https://www.khronos.org/registry/OpenGL/"> OpenGL Registry
    Registry</a>. These are the authoritative documents for how the APIs and
    Shading Languages are intended to work. They are also the most difficult
    to read, being written primarily for device driver implementers, not
    developers using OpenGL ES. </p>

